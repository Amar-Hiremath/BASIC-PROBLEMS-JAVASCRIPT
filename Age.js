//Demonstrate the use of switch case statement in javascript ?
let age = prompt("What is your age ?")
age = Number.parseInt(age)
switch (age) {
    
  case 12 : 
    console.log("your age is 12 & your are amazing!")
break
    
  case 13 :
    console.log ("your age is 13 and your are cool!")
break

  case 14 : 
    console.log ("your age is 14 and your are awesome!")
break
    
  case 15 : 
    console.log ("your age is 15 and your are like thunder!")
break
    
    default :
    console.log ("invalid age , sorry dear :) ")
        
}
